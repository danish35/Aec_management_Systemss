package com.example.aecmanagementsystem;

public class upload_pdf {

    public String name;
    public String url;


    public upload_pdf() {
    }

    public upload_pdf(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}
